#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*temp;

	if (!lst)
		return (NULL);
	temp = lst->next;
	while (temp)
	{
		lst = lst->next;
		temp = lst->next;
	}
	return (lst);
}
